package Expressoes.src;

public class Aritmetica {

    public static int soma(int x, int y){
        return x + y;
    }
    public static int subtracao(int x, int y){
        return x - y;
    }
    public static int multiplicao(int x, int y){
        return x * y;
    }
    public static int divisao(int x, int y){
        return x/y;
    }
}
